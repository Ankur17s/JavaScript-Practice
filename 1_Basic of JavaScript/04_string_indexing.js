// String indexing

let firstName = "ankur";

// a  n  k  u  r 
// 0  1  2  3  4 -- index  

console.log(firstName.length); 
// it gives length of the string

console.log(firstName[0]);
// it gives the character of that particulat index

console.log(firstName.length-1);
// it gives the last index of the string 

console.log(firstName[firstName.length - 1]);
// it gives the last character of the string 

// Note: index also count the space