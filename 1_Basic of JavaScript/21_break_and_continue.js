// break keyword
// it is used to stop that particular execution from that statement
// example:
for(let i = 0; i <= 5; i++){
    if(i === 2){
        break;
    }
    console.log(i);
}

// continue keyword
// it is used skip that particular statement
// example

for(let i = 0; i <= 5; i++){
    if(i === 2){
        continue;
    }
    console.log(i);
}
