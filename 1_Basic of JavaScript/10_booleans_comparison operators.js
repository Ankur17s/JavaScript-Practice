// comparison operator ---->
// it is used to compare the values of variables

// booleans 
// true, false

let num1 = 5;
let num2 = 15;
console.log(num1 > num2);


// == vs ===  ---->
// == only checks the values of the datatype
// === it checks the values as well as type of the datatye

let val1 = "3";
let val2 = 3;
console.log(val1 == val2);
console.log(val1 === val2);


// != a vs !== ------>
let value1 = 2;
let value2 = "2";
console.log(value1 !== value2);
